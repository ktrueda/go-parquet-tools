// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "go-parquet-tools",
	Short: "parquet-tools implemented in golang",
	Long: `go-parquet-tools is faster parquet-tools implmemtend in golang.
	
features are below:
- inspect: inspect a parquet file
- csv: show a parquet file as csv
- show: show	a parquet file as table style

see https://github.com/ktrueda/go-parquet-tools for more details.,
`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

